function pick(arr, seed, salt) {
  if (!arr || !arr.length) return '';
  const i = seed ? hashStr(seed + '|' + salt) % arr.length
                 : Math.floor(Math.random() * arr.length);
  return arr[i];
}

function fillNames(text, names) {
  const a = (names && names[0]) ? names[0] : 'Вы';
  const b = (names && names[1]) ? names[1] : 'он';
  return text.replace(/\{a\}/g, a).replace(/\{b\}/g, b);
}

// Головна точка: повертає об'єкт відповіді.
// opts: { category, premium, names: [a, b], seed }
function buildAnswer(seed, opts = {}) {
  let category = CATEGORY_KEYS.includes(opts.category) ? opts.category : 'general';
  const premium = !!opts.premium;

  // Преміум-категорії доступні лише з підпискою — тихий даунгрейд.
  if (CATEGORIES[category].premium && !premium) category = 'general';

  const base = getOracleAnswer(seed);
  const polarity = base.color; // 'yes' | 'no' | 'maybe'

  // Безкоштовно або немає розбору для теми — короткий вердикт як було.
  const pool = READINGS[category] && READINGS[category][polarity];
  if (!premium || !pool) {
    return { ...base, category, deep: false };
  }

  // Преміум: глибокий персональний розбір + порада.
  const names = opts.names;
  const readingRu = fillNames(pick(pool.ru, seed, 'rd'), names);
  const readingUa = fillNames(pick(pool.ua, seed, 'rd'), names);
  const adviceRu  = pick((ADVICE[category] || ADVICE.general).ru, seed, 'adv');
  const adviceUa  = pick((ADVICE[category] || ADVICE.general).ua, seed, 'adv');

  return {
    ...base,
    message:    `${readingRu}\n\n${adviceRu}`,
    message_ua: `${readingUa}\n\n${adviceUa}`,
    category,
    deep: true,
  };
}

module.exports = { buildAnswer, CATEGORIES, CATEGORY_KEYS };
